public class Person {

    private int age;
    private String address;
    private double height;
    private String race;
    private String gender;

    public Person() {
    }

    public Person(int age, String address, double height, String race, String gender) {
        this.age = age;
        this.address = address;
        this.height = height;
        this.race = race;
        this.gender = gender;
    }

}
